//consecutive <= before that day(index)
//finding the nearest greater on left basically


void solve() {
    vector<int> arr = {100, 80, 60, 70, 60, 75, 85};
    int n = arr.size();
    vector<int> span(n); // to store the span for each day
    stack<int> st; // to store indices

    for (int i = 0; i < n; ++i) {
        // Pop elements from the stack while stack is not empty and
        // the price at top of stack is less than or equal to current price
        while (!st.empty() && arr[st.top()] <= arr[i]) {
            st.pop();
        }

        // If stack is empty, it means no greater element on left
        span[i] = st.empty() ? (i + 1) : (i - st.top());

        // Push this element's index to stack
        st.push(i);
    }

    // Output the result
    for (int s : span) {
        cout << s << " ";
    }
    cout << endl;
}

